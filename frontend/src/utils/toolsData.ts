export const allTools: ToolData[] = [...imageTools, ...pdfTools];

export function getToolById(id: string): ToolData | undefined {
  return allTools.find(t => t.id === id);
}

export function getRelatedTools(toolId: string): ToolData[] {
  const tool = getToolById(toolId);
  if (!tool) return [];
  return tool.relatedTools.map(id => getToolById(id)).filter(Boolean) as ToolData[];
}
